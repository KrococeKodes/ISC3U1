public class Count1
{

    public static void main(String[] args)
    {
        //declaring variables
    	int i;
        
        
    	//printing out the numbers 0-50 by 5
    	i = 0;
    	while (i<=10)
    	{	
    		System.out.println(i*5);
    		i++;
    		
    	}
    }
}